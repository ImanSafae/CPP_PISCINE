#include "Weapon.h"

// ************************************************************************** //
//                          Constructor & Destructor                          //
// ************************************************************************** //

Weapon::Weapon( std::string weapon_type )
{
	setType(weapon_type);
	return ;
}

Weapon::~Weapon( void )
{
	return ;
}

// ************************************************************************** //
//                                  Accessors                                 //
// ************************************************************************** //

void	Weapon::setType( std::string type )
{
	this->_type = type;
}

std::string	&Weapon::getType( void )
{
	return this->_type;
}
